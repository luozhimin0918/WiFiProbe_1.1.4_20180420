    //根据文件名，判断是图片还是视频
    public static int getAdTypeFromFileName(String fileName) {
        String suffix = fileName.substring(fileName.lastIndexOf(".") + 1);
        if (suffix.equals("jpg") || suffix.equals("jpeg") || suffix.equals("gif") || suffix.equals("bmp")) {
            return 0;
        } else if (suffix.equals("mp4") || suffix.equals("avi") || suffix.equals("rmvb") || suffix.equals("rm") || suffix.equals("mpeg") || suffix.equals("wmv")) {
            return 1;
        }
        return -1;
    }

    //将文件从assets复制到指定目录
    public static boolean copyFileFromAssets(Context context, String assetFileName, String saveFileDir,String filaName) {
        InputStream inputStream = null;
        FileOutputStream fileOutputStream = null;
        FileUtils.judeDirExists(new File(saveFileDir));
        String saveFilePath = saveFileDir + File.separator + filaName;
        try {
            inputStream = context.getClass().getClassLoader().getResourceAsStream("assets/"+filaName);
            File file = new File(saveFilePath);
            file.createNewFile();
            fileOutputStream = new FileOutputStream(file);
            byte[] buffer = new byte[4096];
            int byteCount = 0;
            while ((byteCount = inputStream.read(buffer)) != -1) {
                fileOutputStream.write(buffer, 0, byteCount);
            }
            fileOutputStream.flush();
            inputStream.close();
            fileOutputStream.close();
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                if(inputStream!=null)
                inputStream.close();
                if(fileOutputStream!=null)
                fileOutputStream.close();
            } catch (IOException e) {
                Log.d("FileUtils", "CopyFileFromAssets failed :" + e.toString());
                return false;
            }
        }
        return true;
    }

